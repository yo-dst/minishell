#include "libft.h"

t_ull	ft_itb(int nb)
{
	int		i;
	t_ull	res;
	int		tmp[10];

	res = 0;
	i = 0;
	while (nb)
	{
		tmp[i++] = nb % 2;
		nb /= 2;
	}
	while (--i >= 0)
	{
		res *= 10;
		res += tmp[i];
	}
	return (res);
}
